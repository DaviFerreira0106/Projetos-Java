package fundamentos;

public class Wrappers {

    public static void main(String[] args) {
        // Tipo Wrapper dos inteiros
        Byte objB = 100;
        Short objS = 1000;
        Integer objI = 10000;
        Long objL = 100000L;

        // Tipo Wrapper dos pontos flutuantes
        Float objF = 100.12F;
        Double objD = 212912.298292;

        // Tipo Wrapper do literal e lógico
        Character objC = '#';
        Boolean objBool = true;
    }
}
